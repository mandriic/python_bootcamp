wrds = sys.argv[1:]
print(wrds)
to_print = ""
for w in wrds:
    for let in w:
        for m_key, m_val in MORSE.items():
            if let.lower() == m_val:
                to_print += m_key
                to_print += ' '
    to_print += '/ '
print(to_print[:-2])